    private void btn_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_igualActionPerformed
        // TODO add your handling code here:
        this.num2 = Float.parseFloat(this.lbl_IngresoNumero.getText());
        this.lbl_opera.setText("");

        switch (this.operador) {
            case "+":
            this.lbl_IngresoNumero.setText(noCero(this.num1 + this.num2));
            break;
            case "-":
            this.lbl_IngresoNumero.setText(noCero(this.num1 - this.num2));
            break;
            case "*":
            this.lbl_IngresoNumero.setText(noCero(this.num1 * this.num2));
            break;
            case "/":
            if (this.num2 == 0) {
                this.lbl_IngresoNumero.setText("ImposibleDividirEntreCero");
            } else {
                this.lbl_IngresoNumero.setText(noCero(this.num1 / this.num2));
            }
            break;
        }
        String resultado = this.lbl_IngresoNumero.getText();
        this.lbl_opera.setText(this.lbl_opera.getText() + "=");
        historial(this.num1 + " " + this.operador + " " + this.num2 + " = " + resultado);
    }//GEN-LAST:event_btn_igualActionPerformed

