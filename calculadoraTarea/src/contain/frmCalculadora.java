    public String noCero(float resultado) {
        String retorno = "";
        retorno = Float.toString(resultado);
        
        if (resultado % 1 == 0) {
            retorno = retorno.substring(0, retorno.length() - 2);
        }
        return retorno;
    }
    
    private void historial(String operacion) {
        try {
            BufferedWriter writer = new BufferedWriter(new FileWriter("mostrarHistorial.txt", true));
            writer.append(operacion);
            writer.newLine();
            writer.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

